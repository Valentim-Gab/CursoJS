//BEGIN
var i = 1

//WHILE
while (i <= 10) {
    console.log(`WHILE ${i}`)
    i++
}

i = 1

//DO WHILE
do {
    console.log(`DO ${i}`)
    i++
} while (i <= 10)

//FOR
for (i=1; i<=10; i++) {
    console.log(`FOR ${i}`)
}

//END
console.log(`\n======================================`)